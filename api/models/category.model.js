import mongoose from 'mongoose';

const categorySchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      unique: true,
      trim: true,
    },
    slug: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
    },
    
  },
  // adds createdAt and updatedAt fields
);
const Category = mongoose.model('Category', categorySchema,'categories');
export default Category
